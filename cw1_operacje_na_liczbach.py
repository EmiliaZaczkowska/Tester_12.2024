# dane od uzytkownika
number1 = float(input("Podaj pierwsza liczbe"))
number2 = float(input("Podaj druga liczbe"))

# operacje matematyczne
add = number1 + number2

potegowanie = number1 ** number2

# wyswietlenie wyniku
print(f"Suma Twoich liczb {number1}, {number2} to: {add}")
print(f"{number1} do potegi  {number2} to: {potegowanie}")